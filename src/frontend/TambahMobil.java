    public void tampilkanData(){
        Object[] namaKolom = new Object[]{"ID", "Merk", "Tipe", "Tahun","NoPol","Harga Sewa", "Status"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Mobil> listMobil = Mobil.getAll();
        Object[] rowData = new Object[7];
        
        for(Mobil user : listMobil){
            rowData[0] = user.getIdMobil();
            rowData[1] = user.getMerk();
            rowData[2] = user.getTipe();
            rowData[3] = user.getTahun();
            rowData[4] = user.getNoPol();          
            rowData[5] = user.getHargaSewa();
            rowData[6] = user.getStatus();
            
            model.addRow(rowData);
        }
        tblMobil.setModel(model);
    }
    
    public void cari(String keyword){
        Object[] namaKolom = new Object[]{"ID", "Merk", "Tipe", "Tahun","NoPol","Harga Sewa", "Status"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Mobil> listMobil = Mobil.search(keyword);
        Object[] rowData = new Object[7];
        
        for(Mobil user : listMobil){
            rowData[0] = user.getIdMobil();
            rowData[1] = user.getMerk();
            rowData[2] = user.getTipe();
            rowData[3] = user.getTahun();
            rowData[4] = user.getNoPol();          
            rowData[5] = user.getHargaSewa();
            rowData[6] = user.getStatus();
            
            model.addRow(rowData);
        }
        tblMobil.setModel(model);
    }

