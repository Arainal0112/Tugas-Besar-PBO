    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String merk = txtMerk.getText();
        String tipe = txtTipe.getText();
        String tahun = txtTahun.getText();
        String noPol = txtNoPol.getText();
        

        if(!(merk.isEmpty() && tipe.isEmpty()&& tahun.isEmpty()&& noPol.isEmpty())){
            Mobil user = new Mobil();
            user.setIdMobil(Integer.parseInt(txtIdMobil.getText()));
            user.setMerk(merk);
            user.setTipe(tipe);
            user.setTahun(tahun);
            user.setNoPol(noPol);
            user.setHargaSewa(Integer.parseInt(txtHarga.getText()));
            user.setStatus("Tersedia");
            user.save();

            tampilkanData();
        }else{
            JOptionPane.showMessageDialog(null, "Silahkan isi Nama dan Alamat");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

